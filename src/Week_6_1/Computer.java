package Week_6_1;

//this example demonstrates getter -setters
//setters: setting fields
//getters: getting fields values

public class Computer {
	
	private String brand;
	private int price;
	
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
	

}
